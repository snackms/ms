// Fonction pour changer la langue
function changeLanguage(lang) {
    const translation = translations[lang];

    // Mettre à jour les éléments du site avec les traductions
    document.title = translation.pageTitle;

    document.querySelector("a[href='#menu']").textContent = translation.menuLink;
    document.querySelector("a[href='#precommande']").textContent = translation.precommandeLink;
    document.querySelector("a[href='#contact']").textContent = translation.contactLink;
    document.querySelector("a[href='#about']").textContent = translation.aboutLink;

    document.getElementById("hero-title").textContent = translation.heroTitle;
    document.getElementById("hero-description").textContent = translation.heroDescription;

    document.getElementById("menu-title").textContent = translation.menuTitle;

    document.querySelectorAll(".menu-item").forEach((item, index) => {
        const title = item.querySelector("h3");
        const description = item.querySelector("p");

        switch (index) {
            case 0:
                title.textContent = translation.rizCantonais;
                description.textContent = translation.rizCantonaisDesc;
                break;
            case 1:
                title.textContent = translation.nouilles;
                description.textContent = translation.nouillesDesc;
                break;
            case 2:
                title.textContent = translation.nems;
                description.textContent = translation.nemsDesc;
                break;
            case 3:
                title.textContent = translation.legumes;
                description.textContent = translation.legumesDesc;
                break;
            case 4:
                title.textContent = translation.rizBlanc;
                description.textContent = translation.rizBlancDesc;
                break;
            default:
                break;
        }
    });

    document.querySelector(".footer p").innerHTML = translation.footerText;

    document.getElementById("selected-language").textContent = lang === "fr" ? "Français" : lang === "en" ? "English" : lang === "es" ? "Español" : lang === "creole" ? "Kreyòl" : lang === "de" ? "Deutsch" : "中文";
}

// Fonction pour afficher ou masquer le menu de sélection des langues
function toggleLanguageDropdown(event) {
    const dropdown = event.currentTarget.querySelector('.language-dropdown');
    dropdown.classList.toggle('show');
}

// Charger la langue par défaut (français)
window.onload = function() {
    changeLanguage('fr');
};
